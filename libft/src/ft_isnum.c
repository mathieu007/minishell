#include "libft.h"

bool	ft_isnum(char *str)
{
	if (str == NULL)
		return (false);
	if (*str == '-')
		str++;
	while (*str && (*str >= '0' && *str <= '9'))
		str++;
	if (*str == '\0')
		return (true);
	return (false);
}
