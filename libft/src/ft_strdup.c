#include "libft.h"

static char	*ft_strcpy(char *dest, const char *src)
{
	int	i;

	if (!dest || !src)
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(const char *s1)
{
	char	*dest;
	int		src_l;

	if (!s1)
		return (ft_strdup(""));
	src_l = ft_strlen(s1);
	dest = malloc(sizeof(char) * (src_l + 1));
	dest[src_l] = '\0';
	if (!dest)
		return (NULL);
	dest = ft_strcpy(dest, s1);
	if (!dest)
		return (free(dest), NULL);
	return (dest);
}

